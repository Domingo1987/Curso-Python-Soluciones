# Variables de temperatura en Celsius
celsius = 25  # Puedes cambiar este valor para probar con diferentes temperaturas

# Conversión a Fahrenheit
fahrenheit = (celsius * 9/5) + 32

# Conversión a Kelvin
kelvin = celsius + 273.15

# Variables para visualizar los resultados (útiles para depuración o integración en otro código)
resultado_fahrenheit = fahrenheit
resultado_kelvin = kelvin
